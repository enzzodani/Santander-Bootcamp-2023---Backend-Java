public class incr {
    public static void main(String[] args) {
        int num = 5;

        System.out.println(num);
        //x repeat
        num++; //num++ => num = num + 1
        System.out.println(num);
        num--; //num-- => num = num - 1
        System.out.println(num);

        boolean variable = true;

        System.out.println(variable);
        System.out.println(!variable);

        variable = !variable;
        System.out.println(variable);
    }
}
